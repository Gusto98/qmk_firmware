static void render_status(void) {

    if(IS_LAYER_ON(_SWAP)) {
        oled_write_P(PSTR("Toggle Layers:\n"), false);
        oled_write_P(PSTR("ESC: Settings \n"), false);
        oled_write_P(PSTR(" Q : FPS Games\n"), false);
    } else {
        // QMK Logo and version information
        //render_qmk_logo();
        //oled_write_P(PSTR("Kyria rev 4.0\n\n"), false);

        // Host Keyboard Layer Status
        //oled_write_P(PSTR("Layer: "), false);
        switch (get_highest_layer(layer_state)) {
            case _WORKMAN:
                oled_write_P(PSTR("Workman\n"), false);
                render_gusgus_thoughtful();
                break;
            case _FPSGAMES:
                oled_write_P(PSTR("FPS Games\n"), false);
                render_gusgus_panic();
                break;
            case _SETTINGS:
                oled_write_P(PSTR("Settings\n"), false);
                render_gusgus_thoughtful2();
                break;
            case _NUMBERS:
                oled_write_P(PSTR("Numbers\n"), false);
                break;
            case _FKEYSMEDIA:
                oled_write_P(PSTR("Fkeys & Media\n"), false);
                break;
            case _SYMBOLS:
                oled_write_P(PSTR("Symbols\n"), false);
                break;
            case _MOUSEANDNAV:
                oled_write_P(PSTR("Mouse & Nav\n"), false);
                break;
            default:
                oled_write_P(PSTR("Undefined\n"), false);
                render_gusgus_stacked();

        }
    }

    // Host Keyboard LED Status
    uint8_t led_usb_state = host_keyboard_leds();
    oled_write_P(IS_LED_ON(led_usb_state, USB_LED_CAPS_LOCK)   ? PSTR("CAPLCK ") : PSTR("       "), false);
    oled_write_P(IS_LED_ON(led_usb_state, USB_LED_NUM_LOCK)    ? PSTR("NUMLCK ") : PSTR("       "), false);
    oled_write_P(IS_LED_ON(led_usb_state, USB_LED_SCROLL_LOCK) ? PSTR("SCRLCK ") : PSTR("       "), false);
}

void oled_task_user(void) {
    if (is_keyboard_master()) {
        //render_kyria_logo();
        render_status(); // Renders the current keyboard state (layer, lock, caps, scroll, etc)
    } else {
        //render_kyria_logo();
        render_status();
    }
}
